package com.automatedparkinglot.enums;

/**
 * An enumeration to list the statuses of a parking PARKING_IN_PROGRESS: the car is still parking in
 * the assigned parking slot PARKING_OVER: parking is over and the parked car has left the parking
 * lot
 */
public enum ParkingStatus {

  PARKING_IN_PROGRESS,
  PARKING_OVER
}
